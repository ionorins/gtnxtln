#include <string.h>
#include <stdlib.h>
#include "libft.h"

int		ft_isalnum(int c)
{
	int		r;

	r = 0;
	if (ft_isalpha(c) != 0 || ft_isdigit(c) != 0)
		r = 1;
	return (r);
}
