#include <string.h>
#include <stdlib.h>
#include "libft.h"

int		ft_isalpha(int c)
{
	int		r;

	r = 0;
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		r = 1;
	return (r);
}
